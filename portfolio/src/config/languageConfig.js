// Get translation function
export function getTranslation(lang, key) {
  const langObj = TRANSLATIONS[lang];
  if (!langObj) return key;
  const keys = key.split('.');
  let result = langObj;
  for (const k of keys) {
    result = result?.[k];
    if (!result) return key;
  }
  return result;
} 